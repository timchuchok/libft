#include "libft.h"

int		ft_atoi(const char *s)
{
	int res;
	int sign;

	sign = 1;
	res = 0;
	while ((*s >= 9 && *s <= 13) || *s == 32)
		s++;
	if (*s == '-')
	{
		sign = -1;
		s++;
	}
	else if (*s == '+')
		s++;
	while (*s >= 48 && *s <= 57)
	{
		res = res * 10 + *s - 48;
		s++;
	}
	return (res * sign);
}
